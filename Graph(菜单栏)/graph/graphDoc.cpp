// CgraphDoc 命令


CCircle* CgraphDoc::getcircle(int n)								//获取指定序号线段对象的指针
{
	if(n<0||n>m_circlearray.GetUpperBound())
		return NULL;
	return m_circlearray.GetAt(n);
}
void CgraphDoc::addcircle(CPoint p1,CPoint p2,COLORREF color)					//向动态数组添加对象
{
	CCircle *pcircle=new CCircle(p1,p2,color);
	m_circlearray.Add(pcircle);
}
int CgraphDoc::getnumcircle()									//获取线段数量
{
	return m_circlearray.GetSize();
}

